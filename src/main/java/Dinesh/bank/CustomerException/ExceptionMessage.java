
package Dinesh.bank.CustomerException;

public class ExceptionMessage {
	
	public String message;
	public String url;
	public String getMessage() {
		return message;
	}
	public void setMessage(String msg) {
		this.message = msg;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	

}
